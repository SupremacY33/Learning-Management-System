package com.example.leavemanagementsystem;

public class leaves{
        public String name;
        public String fromDate;
        public String toDate;
        public String reason;


        public leaves(String name, String fromDate, String toDate, String reason) {
            this.name = name;
            this.fromDate = fromDate;
            this.toDate = toDate;
            this.reason = reason;
        }
}
